package cards;

public class Circular_Item {

    private String url;
    private String title;
    private String description;

    public Circular_Item() {

    }


    public String getUrl() {
        return url;
    }

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }

    public Circular_Item(String url, String title, String description) {
        this.url = url;
        this.title = title;
        this.description = description;
    }



}
